//time elapsed function that calculated time spent during level in seconds 
//will be DISPLAYED apon level completion
//option can be displayed as part of HUD//
//tested and works

var startTime, endTime;

function start() {
  startTime = new Date();
};

function end() {
  endTime = new Date();
  var timeDiff = endTime - startTime; //in ms
  // strip the ms
  timeDiff /= 1000;

  // get seconds 
  var seconds = Math.round(timeDiff);
  console.log(seconds + " seconds");
}
